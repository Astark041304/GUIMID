             dbConnector dbc = new dbConnector();
             
             
         
             
          String ProductOrderQuery = "SELECT * FROM tbl_product WHERE order_id";
           int rowsAffectedProduct = dbc.executeUpdate( ProductOrderQuery);
  
           String deleteOrderQuery = "DELETE FROM tbl_order";
